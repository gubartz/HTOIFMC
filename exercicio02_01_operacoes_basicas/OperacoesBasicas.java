package exercicio02_01_operacoes_basicas;

public class OperacoesBasicas {


    /**
     * Esse método recebe dois números e retorna o resultado da adição.
     * @param num1 Representa uma parcela.
     * @param num2 Representa uma parcela.
     * @return Retorna o resultado da adição.
     */
    public double somar(double num1, double num2) {
        return num1 + num2;
    }


    /**
     * Esse método recebe dois números e retorna o resultado da subtração.
     * @param num1 Representa o minuendo.
     * @param num2 Representa o subtraendo.
     * @return Retorna o resultado da subtração.
     */
    public double subtrair(double num1, double num2) {
        return num1 - num2;
    }

    /**
     * Esse método recebe dois números e retorna o resultado da multiplicação.
     * @param num1 Representa um fator.
     * @param num2 Representa um fator.
     * @return Retorna o resultado da multiplicação.
     */
    public double multiplicar(double num1, double num2) {
        return num1 * num2;
    }

    /**
     * Esse método recebe dois números e retorna o resultado da divisão.
     * @param num1 Representa o numerador.
     * @param num2 Representa o denominador.
     * @return Retorna o resultado da divisão.
     */
    public double dividir(double num1, double num2) {
        return num1 / num2;
    }
}
